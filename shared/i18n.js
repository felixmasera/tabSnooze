var _currentLang = 'en';

function setLang(lang) {
  _currentLang = LOCALES[lang] ? lang : 'en';
}

function getLang() {
  return _currentLang;
}

function t(key) {
  return (LOCALES[_currentLang] && LOCALES[_currentLang][key] !== undefined
    ? LOCALES[_currentLang][key]
    : (LOCALES.en[key] !== undefined ? LOCALES.en[key] : key));
}

function applyI18n(root) {
  const el = root || document;
  el.querySelectorAll('[data-i18n]').forEach(function(node) {
    node.textContent = t(node.dataset.i18n);
  });
  el.querySelectorAll('[data-i18n-title]').forEach(function(node) {
    node.title = t(node.dataset.i18nTitle);
  });
}
